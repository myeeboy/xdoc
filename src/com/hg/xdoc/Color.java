    private String color;
    public Color(String color) {
        this.color = color;
    }
    public Color(int red, int green, int blue) {
        this(red, green, blue, 255);
    }
    /**
     * 颜色
     * @param red
     * @param green
     * @param blue
     * @param alpha
     */
    public Color(int red, int green, int blue, int alpha) {
    	StringBuffer sb = new StringBuffer("#");
    	sb.append(lpad(Integer.toHexString(red), 2, "0"));
    	sb.append(lpad(Integer.toHexString(green), 2, "0"));
    	sb.append(lpad(Integer.toHexString(blue), 2, "0"));
    	if (alpha != 255) {
    		sb.append(lpad(Integer.toHexString(alpha), 2, "0"));
    	}
    	this.color = sb.toString();
	}
    public String toString() {
        return this.color;
    }
    /**
     * nStr
     * @param str
     * @param n
     * @return
     */
    private static String strn(String str, int n) {
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < n; i++) {
            sb.append(str);
        }
        return sb.toString();
    }
    /**
     * 左补串
     * @param str
     * @param nLen
     * @param pStr
     * @return
     */
    private static String lpad(String str, int nLen, String pStr) {
        if (str.length() < nLen) {
            return strn(pStr, (nLen - str.length()) / pStr.length()) + str;
        } else {
            return str.substring(str.length() - nLen, str.length());
        }
    }
	public static String toString(Color color) {
		return color != null ? color.toString() : "";
	}
